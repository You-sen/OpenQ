@router.post("/otp/send", response_model=OTPSendResponse, summary="Send OTP to candidate email via external backend")
async def otp_send(session_id: str = Form(...)):
    """
    Delegates OTP sending to your external auth backend.
    Reads the candidate's email from the Redis session, then calls OTP_BACKEND_SEND_URL.
 
    Your external backend handles: OTP generation, email delivery, expiry.
    We store nothing OTP-related in Redis.
 
    Set in .env:
      OTP_BACKEND_SEND_URL=https://auth.yourbackend.com/otp/send
    """
    session = hiring_cache.get_session(session_id)
    if not session:
        raise HTTPException(status_code=404, detail="Session not found")
 
    email = session.get("candidate", {}).get("email")
    if not email:
        raise HTTPException(status_code=400, detail="Email not yet collected in this session")

    if not settings.OTP_CHECKER:
        # Generate random 4-digit OTP
        mock_otp = "".join(random.choices(string.digits, k=4))
        session["otp"] = mock_otp
        session["otp_expires"] = time.time() + OTP_TTL_SECONDS
        hiring_cache.save_session(session_id, session)
        logger.info(f"[DEV] Mock OTP generated for {email}: {mock_otp} (session {session_id})")
        return OTPSendResponse(
            success=True,
            message=f"Verification code sent to {email}",
        )

    if not OTP_BACKEND_SEND_URL:
        raise HTTPException(status_code=500, detail="OTP_BACKEND_SEND_URL not configured in .env")
 
    try:
        async with httpx.AsyncClient(timeout=OTP_BACKEND_TIMEOUT) as http:
            response = await http.post(
                OTP_BACKEND_SEND_URL,
                json={"email": email},           # adjust payload shape to match your backend
            )
            response.raise_for_status()
    except httpx.TimeoutException:
        raise HTTPException(status_code=504, detail="OTP service timed out. Please try again.")
    except httpx.HTTPStatusError as e:
        logger.error(f"OTP send failed for {email}: {e.response.status_code} {e.response.text}")
        raise HTTPException(status_code=502, detail="OTP service returned an error. Please try again.")
    except Exception as e:
        logger.error(f"OTP send unexpected error: {e}", exc_info=True)
        raise HTTPException(status_code=500, detail="Could not reach OTP service.")
 
    logger.info(f"OTP send delegated to external backend for {email} (session {session_id})")
    return OTPSendResponse(
        success=True,
        message=f"Verification code sent to {email}",
    )
 
 
