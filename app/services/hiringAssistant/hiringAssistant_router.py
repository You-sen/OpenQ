def require_session(session_id: str) -> dict:
    """FastAPI dependency — raises 404 if session not in Redis."""
    session = hiring_cache.get_session(session_id)
    if not session:
        raise HTTPException(
            status_code=404,
            detail=f"Session '{session_id}' not found or has expired. Please start a new session.",
        )
    return session


