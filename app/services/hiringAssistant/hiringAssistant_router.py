@router.delete("/audio/{session_id}", summary="Delete TTS audio files for a session from S3")
async def delete_session_audio(session_id: str):
    """
    Called by frontend when the session is closed (after receiving close_session action).
 
    Deletes all TTS audio files that were generated during the session.
    Two strategies used in order:
      1. If Redis session still exists: use the tracked tts_turns list (precise)
      2. If Redis is already wiped: fallback to S3 prefix scan (audio/tts/{session_id}_*)
 
    Frontend should call this endpoint:
      - After receiving action: close_session in the chat response
      - Or when the user closes/navigates away from the chat window
 
    Returns:
      { "deleted": N, "errors": [] }
    """
    deleted_count = 0
    errors = []
 
    # Strategy 1: use tracked keys from Redis if session still alive
    tts_keys = hiring_cache.get_tts_keys(session_id)
    if tts_keys:
        logger.info(f"Deleting {len(tts_keys)} tracked TTS keys for session {session_id}")
        result = delete_s3_keys_batch(tts_keys)
        deleted_count = result.get("deleted_count", 0)
        errors = result.get("errors", [])
    else:
        # Strategy 2: Redis already wiped — scan S3 by prefix
        logger.info(f"No Redis keys found for {session_id}, falling back to S3 prefix scan")
        result = delete_s3_prefix(f"audio/tts/{session_id}_")
        deleted_count = result.get("deleted_count", 0)
        errors = result.get("errors", [])
 
    # Also wipe Redis session if it's somehow still there
    if hiring_cache.session_exists(session_id):
        hiring_cache.delete_session(session_id)
 
    logger.info(f"Audio cleanup complete for session {session_id}: {deleted_count} files deleted")
    return {
        "success": len(errors) == 0,
        "session_id": session_id,
        "deleted": deleted_count,
        "errors": errors,
    }
 
 

