@router.post("/resume/upload", response_model=ResumeUploadResponse, summary="Upload candidate resume")
async def resume_upload(
    session_id: str = Form(...),
    resume: UploadFile = File(..., description="Candidate resume PDF"),
):
    """
    Receives the resume PDF, uploads to S3 under resumes/{session_id}.pdf,
    then calls finalize_resume() which:
      - Stores resume URL in session
      - Generates interview summary
      - Builds final candidate payload
      - Wipes Redis session
      - Deletes TTS audio from S3

    The response from this endpoint is the FINAL ChatResponse with:
      action: close_session
      payload: { all candidate data for your other backend }
    """
    session = hiring_cache.get_session(session_id)
    if not session:
        raise HTTPException(status_code=404, detail="Session not found")

    if session.get("step") not in (SessionStep.resume_upload, SessionStep.complete):
        raise HTTPException(
            status_code=400,
            detail=f"Resume upload not expected at step: {session.get('step')}",
        )

    # Validate file type
    content_type = resume.content_type or ""
    filename = resume.filename or ""
    if "pdf" not in content_type.lower() and not filename.lower().endswith(".pdf"):
        raise HTTPException(status_code=400, detail="Only PDF files are accepted")

    # Upload to S3
    s3_key = f"resumes/{session_id}.pdf"
    file_bytes = await resume.read()

    from io import BytesIO
    result = upload_file_object_to_s3(
        file_object=BytesIO(file_bytes),
        object_name=s3_key,
    )

    if not result.get("success"):
        raise HTTPException(status_code=500, detail=f"Resume upload failed: {result.get('message')}")

    resume_url = result["url"]
    logger.info(f"Resume uploaded for session {session_id}: {resume_url}")

    # Finalize: build payload, cleanup, close session
    final_response = await finalize_resume(session_id, resume_url)
    return final_response


