@router.get(
    "/debug/session/{session_id}",
    response_model=DebugSessionResponse,
    summary="[DEBUG] Inspect full Redis session — DISABLE IN PRODUCTION",
)
async def debug_session(session_id: str):
    """
    Returns the full session blob from Redis including:
    - Current step
    - All messages
    - Questions queue with answered/remaining counts
    - Internal scores (DO NOT expose this in production)
    - TTS audio S3 keys

    Use this in Swagger UI (/docs) or Postman to inspect session state during development.
    """
    if os.getenv("ENVIRONMENT", "development") == "production":
        raise HTTPException(
            status_code=403,
            detail="Debug endpoint disabled in production",
        )

    exists = hiring_cache.session_exists(session_id)
    session_data = hiring_cache.raw_session_for_debug(session_id) if exists else None

    return DebugSessionResponse(
        session_id=session_id,
        session=session_data,
        exists=exists,
    )


