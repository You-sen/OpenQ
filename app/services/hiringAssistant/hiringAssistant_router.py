@router.post("/chat/voice", response_model=ChatResponse, summary="Send a voice message")
async def chat_voice(
    session_id: str = Form(...),
    audio: UploadFile = File(..., description="Raw audio file (webm, mp4, wav, m4a, mp3)"),
    job_details: Optional[str] = Form(None, description="JSON string of job details (first message only)"),
):
    """
    Voice input endpoint.
    1. Receives raw audio from frontend.
    2. Transcribes via OpenAI Whisper.
    3. Runs the same pipeline as /chat/text.
    4. Generates TTS audio for the AI reply.
    5. Returns { message, audio_url, action }.

    The frontend should play `audio_url` when it is present.
    """
    import json as _json

    try:
        audio_bytes = await audio.read()
        jd = None
        if job_details:
            try:
                jd = _json.loads(job_details)
            except Exception:
                logger.warning(f"Could not parse job_details JSON for session {session_id}")

        response = await process_voice(
            session_id=session_id,
            audio_bytes=audio_bytes,
            job_details=jd,
        )
        return response
    except Exception as e:
        logger.error(f"chat_voice error [{session_id}]: {e}", exc_info=True)
        raise HTTPException(status_code=500, detail="Internal server error")


