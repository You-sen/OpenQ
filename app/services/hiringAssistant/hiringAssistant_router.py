@router.post("/chat/text", response_model=ChatResponse, summary="Send a text message")
async def chat_text(request: ChatTextRequest):
    """
    Main text chat endpoint. The frontend calls this on every candidate message.

    - On the very first message, pass `job_details` so the AI knows the role context.
    - `session_id` must be generated by the frontend (UUID) and sent on every request.
    - Returns `action: show_file_input` when interview is done (frontend shows upload widget).
    - Returns `action: close_session` with `payload` after resume upload is complete.
    """
    try:
        response = await process_chat(
            session_id=request.session_id,
            user_message=request.message,
            job_details=request.job_details,
            generate_audio=False,
        )
        return response
    except Exception as e:
        logger.error(f"chat_text error [{request.session_id}]: {e}", exc_info=True)
        raise HTTPException(status_code=500, detail="Internal server error")


