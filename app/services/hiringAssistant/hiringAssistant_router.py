@router.post("/otp/verify", summary="Verify OTP code via external backend")
async def otp_verify(request: OTPVerifyRequest):
    """
    Sends the candidate's code to your external backend for verification.
 
    On success (external returns true):
      - Advances session step to otp_verified
      - Returns { "validated": true }
 
    On failure (wrong code / expired):
      - Returns 400 with a clear message
      - Session stays at otp_pending so the candidate can retry or request resend
 
    Frontend flow:
      Wrong code  → show error, let candidate retype (call this endpoint again)
      Resend      → call POST /hiring/otp/send again with the same session_id
 
    Set in .env:
      OTP_BACKEND_VERIFY_URL=https://auth.yourbackend.com/otp/verify
    """
    session = hiring_cache.get_session(request.session_id)
    if not session:
        raise HTTPException(status_code=404, detail="Session not found")
 
    email = session.get("candidate", {}).get("email")
    if not email:
        raise HTTPException(status_code=400, detail="No email found in session")
 
    if not settings.OTP_CHECKER:
        stored_otp = session.get("otp")
        otp_expires = session.get("otp_expires", 0)
        
        if not stored_otp:
            raise HTTPException(status_code=400, detail="No OTP requested.")
            
        if time.time() > otp_expires:
            raise HTTPException(status_code=400, detail="OTP has expired. Please request a new one.")
            
        if request.code.strip() != stored_otp:
            raise HTTPException(status_code=400, detail="Invalid verification code.")
            
        # Success
        session["step"] = SessionStep.otp_verified
        session["otp"] = None
        session["otp_expires"] = None
        hiring_cache.save_session(request.session_id, session)
        logger.info(f"[DEV] Mock OTP verified for session {request.session_id}")
        return {"validated": True}

    if not OTP_BACKEND_VERIFY_URL:
        raise HTTPException(status_code=500, detail="OTP_BACKEND_VERIFY_URL not configured in .env")
 
    # Call external backend to verify
    try:
        async with httpx.AsyncClient(timeout=OTP_BACKEND_TIMEOUT) as http:
            response = await http.post(
                OTP_BACKEND_VERIFY_URL,
                json={
                    "email": email,
                    "code": request.code.strip(),   # adjust field names to match your backend
                },
            )
 
            # If backend returns 4xx, it means wrong code or expired — not a server error
            if response.status_code == 400 or response.status_code == 422:
                detail = _extract_error_message(response)
                raise HTTPException(status_code=400, detail=detail or "Invalid or expired verification code.")
 
            response.raise_for_status()
            body = response.json()
 
    except HTTPException:
        raise   # re-raise our own 400s
    except httpx.TimeoutException:
        raise HTTPException(status_code=504, detail="OTP service timed out. Please try again.")
    except httpx.HTTPStatusError as e:
        logger.error(f"OTP verify error: {e.response.status_code} {e.response.text}")
        raise HTTPException(status_code=502, detail="OTP service returned an error.")
    except Exception as e:
        logger.error(f"OTP verify unexpected error: {e}", exc_info=True)
        raise HTTPException(status_code=500, detail="Could not reach OTP service.")
 
    # Check the response body — your backend may return { "validated": true } or { "success": true }
    # Adjust the key name below to match your backend's actual response shape
    is_valid = (
        body.get("validated") is True
        or body.get("success") is True
        or body.get("verified") is True
        or body.get("valid") is True
    )
 
    if not is_valid:
        raise HTTPException(status_code=400, detail="Invalid or expired verification code. Please try again.")
 
    # Advance session step
    session["step"] = SessionStep.otp_verified
    hiring_cache.save_session(request.session_id, session)
 
    logger.info(f"OTP verified via external backend for session {request.session_id}")
    return {"validated": True}
 
 
def _extract_error_message(response: httpx.Response) -> Optional[str]:
    """Try to pull a human-readable error from the external backend's response body."""
    try:
        body = response.json()
        return (
            body.get("message")
            or body.get("detail")
            or body.get("error")
            or body.get("msg")
        )
    except Exception:
        return None

